#include <fillit.h>

int		handle_o(char *block)
{
	int	x;
	int	y;

	y = -1;
	while (++y <= g_bs - 2)
	{
		x = -1;
		while (++x <= g_bs - 2)
			if (g_b[y][x] == '.' && g_b[y + 1][x + 1] == '.' &&
				g_b[y + 1][x] == '.' && g_b[y][x + 1] == '.')
			{
				g_b[y][x] = block[0];
				g_b[y + 1][x] = block[0];
				g_b[y][x + 1] = block[0];
				g_b[y + 1][x + 1] = block[0];
				if (solve())
					return (1);
				g_b[y][x] = '.';
				g_b[y + 1][x + 1] = '.';
				g_b[y + 1][x] = '.';
				g_b[y][x + 1] = '.';
			}
	}
	return (0);
}
